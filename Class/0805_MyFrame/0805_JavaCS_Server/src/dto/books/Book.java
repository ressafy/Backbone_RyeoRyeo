package dto.books;


public class Book {
	
	private String isbn, title, author, publisher, desc;
	private int price;
	
	public Book() {
		super();
	}

	public Book(String isbn, String title, String author, String publisher, String desc, int price) {
		super();
		setIsbn(isbn);
		setTitle(title);
		setAuthor(author);
		setPublisher(publisher);
		setDesc(desc);
		setPrice(price);		
	}

	@Override
	public String toString() {
		return "Book [isbn=" + isbn + ", title=" + title + ", author=" + author + ", publisher=" + publisher + ", desc="
				+ desc + ", price=" + price + "]";
	}

	public String getIsbn() {
		return this.isbn;
	}

	public void setIsbn(String isbn) {
		if(isbn != null) {
			this.isbn = isbn;
		}
		
	}

	public String getTitle() {
		return this.title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return this.author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getPublisher() {
		return this.publisher;
	}

	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}

	public String getDesc() {
		return this.desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getPrice() {
		return this.price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
